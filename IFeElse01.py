#faça um programa uqe o usuario digite um numero e diga se o numero é superior a 20, inferior a ou se esta entre 10 e 20.
numero = int (input ("Digite um numero: "))

if numero > 20:
    print ("seu numero é maior que 20")
elif numero < 10:
    print ("seu numero é menor que 10")
else:
    print ("seu numero esta entre 10 e 20")